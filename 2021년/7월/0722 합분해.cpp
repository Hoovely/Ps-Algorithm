#include <iostream>
using namespace std;

int n, k, dp[201][201];
long long mod = 1000000000;

int main()
{
	cin >> n >> k;
	for (int i=1;i<n+1;i++) dp[i][1] = 1;
	for (int i=1;i<k+1;i++) dp[1][i] = i;
	for (int i=2;i<n+1;i++)
	{
		for(int j=2;j<k+1;j++) dp[i][j] = (dp[i-1][j] + dp[i][j-1]) % mod;
	}
	
	cout << dp[n][k];
}



